package com.overloading;

public class Multiplication {
	public double multi(double a,int b) {
		return a+b;
		}
		public double multi(int a,double b) {
			return a+b;
			}
		public double multi(int a,int b) {
			return a+b;
			}
		public double multi(int a,int b,int c) {
			return a+b;
			}
		public double multi(int a,double b,int c) {
			return a+b;
			}


}
